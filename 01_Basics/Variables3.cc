#include <iostream>

int main()
{
    // 1 Byte = 8bit
    bool my_value0 = true; // false

    // 1 Byte = 8bit
    char my_value1 = 10;

    // 2 Byte = 16bit
    short my_value2 = 42;

    // 4 Byte = 32bit
    int my_value3 = 22;

    // 4 Byte = 32bit
    float my_value4 = 12.0;

    // 8 Byte = 64bit
    double my_value5 = 13.0;

    return 0;
}
